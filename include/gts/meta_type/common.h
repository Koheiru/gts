#pragma once

// ----------------------------------------------------------------------------
namespace gts
{

// ----------------------------------------------------------------------------
//                              value meta type
// ----------------------------------------------------------------------------
template <class T, class ...Args>
struct value
{
    using target = T;
};

// ----------------------------------------------------------------------------
//                          meta type redirector
// ----------------------------------------------------------------------------
template <class T, class ...Args>
struct meta_type
{
    using value = typename value<T, Args...>;
};

} // namespace gts
