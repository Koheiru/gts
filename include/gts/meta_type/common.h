#pragma once

// ----------------------------------------------------------------------------
namespace gts
{

// ----------------------------------------------------------------------------
//                              value meta type
// ----------------------------------------------------------------------------
template <class ...Args>
struct value;

// ----------------------------------------------------------------------------
template <class T>
struct value<T>
{
    using target = T;
};

// ----------------------------------------------------------------------------
//                          meta type redirector
// ----------------------------------------------------------------------------
template <class ...Args>
struct meta_type;

// ----------------------------------------------------------------------------
template <class T>
struct meta_type<T>
{
    using value = typename value<T>;
};

} // namespace gts
